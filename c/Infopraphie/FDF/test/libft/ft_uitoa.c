#include "libft.h"

char	*ft_uitoa(unsigned int n)
{
	char	*p;

	p = (char*)malloc(sizeof(char) * 22);
	p = p + 21;
	if (n == 0)
		*p = n + 48;
	while (n != 0)
	{
		*--p = (n % 10) + 48;
		n /= 10;
	}
	return (p);
}
