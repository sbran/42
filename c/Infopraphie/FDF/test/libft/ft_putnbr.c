#include "libft.h"

void	ft_putnbr(int a)
{
	if (a == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (a < 0)
		{
			ft_putchar('-');
			a = -a;
		}
		if (a / 10 > 0)
			ft_putnbr(a / 10);
		ft_putchar('0' + a % 10);
	}
}
