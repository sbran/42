#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	char	*tmp;
	int		i;
	
	tmp = (char *)s;
	i = 0;
	while (tmp[i] != (char)c)
	{
		if (!tmp[i])
			return (0);
		i++;
	}
	return (&tmp[i]);
}
