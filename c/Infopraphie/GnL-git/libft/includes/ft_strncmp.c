#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t size)
{
	while (size && *s1 && *s1 == *s2)
	{
		s1++;
		s2++;
		size--;
	}
	if (size == 0)
		return (0);
	return (*s1 - *s2);
}
